# A for loop is used to iterate through a sequence like list, tuple, or string [iterables]


print("List:")
l = [1, 7, 8] 
for item in l: 
    print(item) # prints 1, 7 and 8 


print("String:")
s = "Himansi"
for i in s:
    print(i)


print("Tuple:")
t = (1, False, "Himansi")
for i in t:
    print(i)