# RANGE FUNCTION :- 
# The range() function in python is used to generate a sequence of number. 
# We can also specify the start, stop and step-size as follows: 
# range(start, stop, step_size) 
# step_size is usually not used with range()


# ex 
for i in range(4): # range(4) = range(0, 4) 
    print(i)


# ex 2 step size
for i in range(0, 50, 4): # ex 0 4 8 ....meaning is start with 0 and jump to 4  =4 and jumb 4 = 8 and so on
    print(i)